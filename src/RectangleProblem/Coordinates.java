package RectangleProblem;

/**
 * coordinates of a rectangle within a box
 */
public class Coordinates {
    // width
    private int x;
    //height
    private int y;

    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
